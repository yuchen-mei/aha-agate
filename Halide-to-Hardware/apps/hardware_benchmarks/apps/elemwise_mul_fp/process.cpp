#include <iostream>
#include <math.h>
#include <cstdio>
#include <fstream>
#include <vector>
#include "hardware_process_helper.h"
#include "halide_image_io.h"
#include "coreir.h"
#include "hw_support_utils.h"
#include "cstdlib"

#if defined(WITH_CPU)
   #include "elemwise_mul_fp.h"
#endif

#if defined(WITH_COREIR)
    #include "coreir_interpret.h"
#endif

#if defined(WITH_CLOCKWORK)
    #include "rdai_api.h"
    #include "clockwork_sim_platform.h"
    #include "elemwise_mul_fp_clockwork.h"
#endif

using namespace Halide::Tools;
using namespace Halide::Runtime;

int main( int argc, char **argv ) {
  std::map<std::string, std::function<void()>> functions;
  ManyInOneOut_ProcessController<uint16_t> processor("elemwise_mul_fp", {"hw_partial_sum_input_stencil.mat", "hw_input_b_stencil.raw"});

  #if defined(WITH_CPU)
      auto cpu_process = [&]( auto &proc ) {
      elemwise_mul_fp(proc.inputs["hw_partial_sum_input_stencil.mat"], proc.inputs["hw_input_b_stencil.raw"], proc.output);
      };
      functions["cpu"] = [&](){ cpu_process( processor ); } ;
  #endif

  #if defined(WITH_COREIR)
      auto coreir_process = [&]( auto &proc ) {
          run_coreir_on_interpreter<>( "bin/design_top.json",
                                       proc.inputs["hw_partial_sum_input_stencil.mat"], proc.output,
                                       "self.in_arg_0_0_0", "self.out_0_0" );
      };
      functions["coreir"] = [&](){ coreir_process( processor ); };
  #endif

  #if defined(WITH_CLOCKWORK)
      auto clockwork_process = [&]( auto &proc ) {
        RDAI_Platform *rdai_platform = RDAI_register_platform( &rdai_clockwork_sim_ops );
        if ( rdai_platform ) {
          printf( "[RUN_INFO] found an RDAI platform\n" );
          elemwise_mul_fp_clockwork( proc.inputs["hw_partial_sum_input_stencil.mat"], proc.inputs["hw_input_b_stencil.raw"], proc.output );
          RDAI_unregister_platform( rdai_platform );
        } else {
          printf("[RUN_INFO] failed to register RDAI platform!\n");
        }
      };
      functions["clockwork"] = [&](){ clockwork_process( processor ); };
  #endif

  // Add all defined functions
  processor.run_calls = functions;

  processor.inputs_preset = true;


  auto N = getenv("seq_len");
  auto D = getenv("hidden_dim");
  auto n = N ? atoi(N) : 64;
  auto d = D ? atoi(D) : 128;

  processor.inputs["hw_partial_sum_input_stencil.mat"] = Buffer<uint16_t>(d, n);
  processor.inputs["hw_input_b_stencil.raw"] = Buffer<uint16_t>(d, n);
  processor.output  = Buffer<uint16_t>(d, n);

  int max_rand = 256 - 1;
  // Fill input with random values
  for (int d = 0; d < processor.inputs["hw_partial_sum_input_stencil.mat"].dim(0).extent(); d++) {
      for (int n = 0; n < processor.inputs["hw_partial_sum_input_stencil.mat"].dim(1).extent(); n++) {
        processor.inputs["hw_partial_sum_input_stencil.mat"](d, n) = float_to_bfloat16_process(
            // [-7, 7]
            ((float)rand() / RAND_MAX) * 14.0 - 7.0
          );
    }
  }

   // Fill input with random values
  for (int d = 0; d < processor.inputs["hw_input_b_stencil.raw"].dim(0).extent(); d++) {
      for (int n = 0; n < processor.inputs["hw_input_b_stencil.raw"].dim(1).extent(); n++) {
        processor.inputs["hw_input_b_stencil.raw"](d, n) = float_to_bfloat16_process(
            // [-7, 7]
            ((float)rand() / RAND_MAX) * 14.0 - 7.0
          );
    }
  }

  // Gold output
  for (int n = 0; n < processor.output.dim(1).extent(); n++) {
    for (int d = 0; d <  processor.output.dim(0).extent(); d++) {
        float prod = bfloat16_to_float_process(processor.inputs["hw_partial_sum_input_stencil.mat"](d, n)) * bfloat16_to_float_process(processor.inputs["hw_input_b_stencil.raw"](d, n));
        processor.output(d, n) = float_to_bfloat16_process(prod);
      }
    }

  std::cout << "Writing hw_partial_sum_input_stencil.mat to bin folder" << std::endl;
  save_image(processor.inputs["hw_partial_sum_input_stencil.mat"], "bin/hw_partial_sum_input_stencil.mat");

  // std::cout << "Writing hw_input_b_stencil.raw to bin folder" << std::endl;
  // save_halide_buffer_to_raw(processor.inputs["hw_input_b_stencil.raw"], "bin/hw_input_b_stencil.raw");

  // Copy the pre-existing raw file into the bin folder for now
  int ret = system("cp hw_input_stencil.raw bin/hw_input_b_stencil.raw");
  if (ret != 0) {
    std::cerr << "Error: Failed to copy hw_input_stencil.raw to bin folder. "
                 "hw_input_stencil.raw should have been generated by /aha/voyager/scripts/aha_flow.parse_dnnLayer_tensors.py "
                 "(system call returned " << ret << ")" << std::endl;
    return 1;
  }
  std::cout << "Copying pre-existing hw_input_stencil.raw to bin folder" << std::endl;

  ret = system("cp hw_partial_sum_input_stencil.raw bin/hw_partial_sum_input_stencil.raw");
  if (ret != 0) {
    std::cerr << "Error: Failed to copy hw_partial_sum_input_stencil.raw to bin folder. "
                 "hw_partial_sum_input_stencil.raw should have been generated by /aha/voyager/scripts/aha_flow.parse_dnnLayer_tensors.py "
                 "(system call returned " << ret << ")" << std::endl;
    return 1;
  }
  std::cout << "Copying pre-existing hw_partial_sum_input_stencil.raw to bin folder" << std::endl;

  std::cout << "Writing output to bin folder" << std::endl;
  save_image(processor.output, "bin/hw_output.mat");

  // Create glb bank config
  using namespace glb_cfg;
  // inputs, outputs, mu_inputs
  const config_spec spec = {
      {
          tensor_spec{"hw_input_b_stencil", {"x_coord"}},
          tensor_spec{"hw_partial_sum_input_stencil", {"x_coord"}},
      },
      {
          tensor_spec{"hw_output", {"x_coord"}}
      },
      {}
  };
  write_glb_bank_config(spec);

  return processor.process_command(argc, argv);
}
